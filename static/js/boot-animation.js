let i = 0;

function play() {
    el.textContent = frames[i];
    i++;

    if (i < frames.length) {
        setTimeout(play, 120);
    } else {
        finishBoot();
    }
}

async function finishBoot() {
    await new Promise(r => setTimeout(r, 400));

    try {
        const res = await fetch("/api/session");
        const data = await res.json();

        if (data.logged_in) {
            window.location.href = "/dashboard";
        } else {
            window.location.href = "/login";
        }

    } catch (err) {
        window.location.href = "/login";
    }
}

